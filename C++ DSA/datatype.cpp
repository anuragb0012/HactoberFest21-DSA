#include<iostream>
using namespace std;
int main()
{
    int a;
    a=10;
    cout<<"size of int "<<sizeof(a)<<endl;
    float b;
    cout<<"size of float"<<sizeof(b)<<endl;
    char c;
    cout<<"size of char"<<sizeof(c)<<endl;
    bool d;
    cout<<"size of bool "<<sizeof(d)<<endl;
    short int si;
    cout<<"size of short"<<sizeof(si)<<endl;
    long int pi;
    cout<<"size of long"<<sizeof(pi)<<endl;
}